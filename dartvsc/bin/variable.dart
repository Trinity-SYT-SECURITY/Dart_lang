//常見的變量類型：
/*
int：表示整數類型。
double：表示浮點數類型。
num：表示數字類型，可以是整數或浮點數。
bool：表示布爾類型，可以是 true 或 false。
String：表示字符串類型。
List：表示列表（數組）類型，可以存儲多個值。
Map：表示映射（字典）類型，用於存儲鍵值對。
dynamic：表示動態類型，可以接受任意類型的值。
var：表示自動類型推斷，根據賦值的值自動推斷變量的類型。*/

void main() {
  // declare using var
  var str = 1;
  //declare using type
  String str2 = "hello";
  print("$str $str2");
}
